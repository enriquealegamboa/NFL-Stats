CREATE TABLE IF NOT EXISTS "conference" (
    "conference" CHAR(3) PRIMARY KEY
);

CREATE TABLE IF NOT EXISTS "division" (
    "division" VARCHAR(5),
    "conference" CHAR(3) REFERENCES "conference",
    PRIMARY KEY("division", "conference")
);

CREATE TABLE IF NOT EXISTS "team" (
    "team_id" INT PRIMARY KEY,
    "team_name" VARCHAR(30) NOT NULL,
    "abbreviation" VARCHAR(3) NOT NULL,
    "conference" CHAR(3) NOT NULL,
    "division" VARCHAR(5) NOT NULL,
    foreign KEY ("division", "conference") REFERENCES "division"
);

CREATE TABLE IF NOT EXISTS "season" (
    "season_id" INT GENERATED BY DEFAULT AS IDENTITY (start with 2025) PRIMARY KEY,
    "start_date" DATE NOT NULL,
    "end_date" DATE NOT NULL
);

CREATE TABLE IF NOT EXISTS "game" (
    "game_id" INT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    "week" INT NOT NULL,
    "is_outside" BOOL NOT NULL,
    "bad_weather" BOOL NOT NULL,
    "is_neutral" BOOL DEFAULT false,
    "game_date" DATE NOT NULL,
    "season_id" INT REFERENCES "season" NOT NULL, 
    "home_team_id" INT REFERENCES "team" NOT NULL,
    "away_team_id" INT REFERENCES "team" NOT NULL,
    "home_team_score" INT NOT NULL,
    "away_team_score" INT NOT NULL,

    constraint different_teams
    CHECK( "home_team_id" <> "away_team_id")
);

CREATE TABLE IF NOT EXISTS "position" (
    "position" VARCHAR(2) PRIMARY KEY
);

CREATE TABLE IF NOT EXISTS "player" (
    "player_id" INT PRIMARY KEY,
    "full_name" VARCHAR(45) NOT NULL
);

CREATE TABLE IF NOT EXISTS "player_position" (
    "player_id" INT REFERENCES "player",
    "position" VARCHAR(2) REFERENCES "position",
    PRIMARY KEY("player_id", "position")
);

CREATE TABLE IF NOT EXISTS "roster" (
    "roster_id" INT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    "player_id" INT NOT NULL REFERENCES "player",
    "team_id" INT NOT NULL REFERENCES "team",
    "start_date" DATE NOT NULL,
    "end_date" DATE,

    constraint valid_date_range
    CHECK (end_date IS NULL OR end_date >= start_date)
);

