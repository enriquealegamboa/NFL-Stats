CREATE TABLE IF NOT EXISTS "game_team_stats" (
    "game_id" INT NOT NULL REFERENCES "game",
    "team_id" INT NOT NULL REFERENCES "team",

    -- 1st Downs
    "first_downs_total" INT NOT NULL CHECK ("first_downs_total" >= 0),
    "first_downs_passing" INT NOT NULL CHECK ("first_downs_passing" >= 0),
    "first_downs_rushing" INT NOT NULL CHECK ("first_downs_rushing" >= 0),
    "first_downs_penalty" INT NOT NULL CHECK ("first_downs_penalty" >= 0),

    -- 3rd Down
    "third_down_conversions" INT NOT NULL CHECK ("third_down_conversions" >= 0),
    "third_down_attempts" INT NOT NULL CHECK ("third_down_attempts" >= 0),
    CHECK ("third_down_conversions" <= "third_down_attempts"),

    -- 4th Down
    "fourth_down_conversions" INT NOT NULL CHECK ("fourth_down_conversions" >= 0),
    "fourth_down_attempts" INT NOT NULL CHECK ("fourth_down_attempts" >= 0),
    CHECK ("fourth_down_conversions" <= "fourth_down_attempts"),

    -- Offense
    "total_plays" INT NOT NULL CHECK ("total_plays" >= 0),
    "total_yards" INT NOT NULL,

    "total_drives" INT NOT NULL CHECK ("total_drives" >= 0),

    -- Passing
    "passing_yards" INT NOT NULL,
    "pass_completions" INT NOT NULL CHECK ("pass_completions" >= 0),
    "pass_attempts" INT NOT NULL CHECK ("pass_attempts" >= 0),
    CHECK ("pass_completions" <= "pass_attempts"),

    "interceptions_thrown" INT NOT NULL CHECK ("interceptions_thrown" >= 0),

    "sacks" INT NOT NULL CHECK ("sacks" >= 0),
    "sack_yards_lost" INT NOT NULL CHECK ("sack_yards_lost" >= 0),

    -- Rushing
    "rushing_yards" INT NOT NULL,
    "rushing_attempts" INT NOT NULL CHECK ("rushing_attempts" >= 0),

    -- Red Zone
    "red_zone_made" INT NOT NULL CHECK ("red_zone_made" >= 0),
    "red_zone_attempts" INT NOT NULL CHECK ("red_zone_attempts" >= 0),
    CHECK ("red_zone_made" <= "red_zone_attempts"),

    -- Penalties
    "penalties_count" INT NOT NULL CHECK ("penalties_count" >= 0),
    "penalty_yards" INT NOT NULL CHECK ("penalty_yards" >= 0),

    -- Turnovers
    "turnovers" INT NOT NULL CHECK ("turnovers" >= 0),
    "fumbles_lost" INT NOT NULL CHECK ("fumbles_lost" >= 0),

    -- Defense / Special Teams
    "defensive_special_teams_tds" INT NOT NULL CHECK ("defensive_special_teams_tds" >= 0),

    -- Time of Possession (seconds in full "game")
    "possession_time_seconds" INT NOT NULL CHECK (
        "possession_time_seconds" >= 0
        AND "possession_time_seconds" <= 3600
    ),

    PRIMARY KEY ("game_id", "team_id")
);

